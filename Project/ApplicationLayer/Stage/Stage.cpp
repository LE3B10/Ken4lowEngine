#include "Stage.h"

/// -------------------------------------------------------------
///				　			　初期化処理
/// -------------------------------------------------------------
void Stage::Initialize()
{
	object_ = std::make_unique<Object3D>();
	// 地形オブジェクトの初期化
	object_->Initialize("Stage1.gltf");
	object_->SetTranslate({ 0.0f,0.0f,0.0f });
}

/// -------------------------------------------------------------
///				　			　更新処理
/// -------------------------------------------------------------
void Stage::Update()
{
	object_->Update();
}

/// -------------------------------------------------------------
///				　			　描画処理
/// -------------------------------------------------------------
void Stage::Draw()
{
	object_->Draw();
}